package com.poiexcel.vo;

public class CardDetail {
	private int id;
	private int gprs;
	private double gprsused;
	private double gprsrest;
	private int sms;
	private int smsused;
	private int smsrest;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getGprs() {
		return gprs;
	}

	public void setGprs(int gprs) {
		this.gprs = gprs;
	}

	public double getGprsused() {
		return gprsused;
	}

	public void setGprsused(double gprsused) {
		this.gprsused = gprsused;
	}

	public double getGprsrest() {
		return gprsrest;
	}

	public void setGprsrest(double gprsrest) {
		this.gprsrest = gprsrest;
	}

	public int getSms() {
		return sms;
	}

	public void setSms(int sms) {
		this.sms = sms;
	}

	public int getSmsused() {
		return smsused;
	}

	public void setSmsused(int smsused) {
		this.smsused = smsused;
	}

	public int getSmsrest() {
		return smsrest;
	}

	public void setSmsrest(int smsrest) {
		this.smsrest = smsrest;
	}

}
